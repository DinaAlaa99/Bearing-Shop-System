    public void initArrayList() {
        String query = "SELECT * FROM TRANSACTIONS";
        ArrayList<Transactions> recordData = new ArrayList<>();
        try {
            int counter = 0;
            DBConnection conn = new DBConnection(query);
            conn.DatabaseAccess();

            while (conn.myresobj.next()) {
                Transactions transactions = new Transactions(conn.myresobj.getString("BILLNUMBER"),
                        conn.myresobj.getString("STATUS"),
                        Integer.parseInt(conn.myresobj.getString("D_YEAR")),
                        conn.myresobj.getString("NAME"),
                        Double.parseDouble(conn.myresobj.getString("TOTALPAIDAMOUNT")));

                TransactionsData.add(transactions);

            }
            conn.CloseConnection();
        } catch (SQLException e) {

            e.printStackTrace();
        }

    }

    public void addRowToJTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object rowData[] = new Object[5];
        for (int i = 0; i < TransactionsData.size(); i++) {

            rowData[0] = TransactionsData.get(i).getBillNumber();
            rowData[1] = TransactionsData.get(i).getStatus();
            rowData[2] = TransactionsData.get(i).getD_Year();
            rowData[3] = TransactionsData.get(i).getName();
            rowData[4] = TransactionsData.get(i).getTotalPaidAmount();
            model.addRow(rowData);
        }
    }

