    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        int row = jTable1.getSelectedRow();

        if (row < 0) {
            JOptionPane.showMessageDialog(TransactionsTable.this, "You must select a transaction.", "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        int response = JOptionPane.showConfirmDialog(TransactionsTable.this,
                "Delete this transaction(s)?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (response != JOptionPane.YES_OPTION) {
            return;
        }
        String value = jTable1.getModel().getValueAt(row, 0).toString();
        System.out.println(value);
        try {
            String query = "DELETE FROM TRANSACTIONS WHERE BILLNUMBER = '" + value + "'";
            System.out.println(query);
            try {
                DBConnection conn = new DBConnection(query);
                conn.UpdadateTable();
                //refershTransactionsTable();
                conn.CloseConnection();
                dispose();
                TransactionsTable transactions = new TransactionsTable();
                transactions.setLocationRelativeTo(null);
                transactions.setVisible(true);
            } catch (Exception e) {
                e.printStackTrace();
            }

        } catch (Exception e1) {
            e1.printStackTrace();
        }
    }//GEN-LAST:event_DeleteActionPerformed

