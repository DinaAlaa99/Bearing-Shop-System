    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String search_transaction = search.getText();
        int counter = 0;
        String query = "SELECT * FROM TRANSACTIONS WHERE  BILLNUMBER = '" + search_transaction + "'";
        try {
            DBConnection conn = new DBConnection(query);
            conn.DatabaseAccess();
            while (conn.myresobj.next()) {
                counter++;
            }
            if (counter == 0) {
                JOptionPane.showMessageDialog(null, "TRANSACTION NOT FOUND");
            } else {
                counter = 0;

                TableRowSorter sorter = new TableRowSorter(jTable1.getModel());
                jTable1.setRowSorter(sorter);
                sorter.setRowFilter(RowFilter.regexFilter(search_transaction));
                log_out.setVisible(true);
                conn.CloseConnection();

            }

        } catch (SQLException e) {
            System.out.println("error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

